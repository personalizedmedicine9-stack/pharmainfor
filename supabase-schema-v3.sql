-- ═══════════════════════════════════════════════════════════════════════════════
-- 6. CACHED_QUERIES
-- ═══════════════════════════════════════════════════════════════════════════════
-- Stores cached API responses to avoid redundant external lookups.
-- Access is restricted to the service role — public users should NOT be able
-- to read or manipulate the cache directly.

CREATE TABLE IF NOT EXISTS cached_queries (
  id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  cache_key     TEXT UNIQUE NOT NULL,
  query_type    TEXT,
  query_params  JSONB DEFAULT '{}'::jsonb,
  results       JSONB,
  sources_used  JSONB DEFAULT '[]'::jsonb,
  result_count  INTEGER DEFAULT 0,
  expires_at    TIMESTAMPTZ NOT NULL,
  created_at    TIMESTAMPTZ DEFAULT now()
);

COMMENT ON TABLE cached_queries IS 'API response cache. Access restricted to service role only — public users cannot read or write the cache.';

-- Indexes — cache_key already has a unique index from the UNIQUE constraint,
-- but we add a covering index for expiry-based cleanup.
CREATE INDEX IF NOT EXISTS idx_cached_queries_cache_key ON cached_queries(cache_key);
CREATE INDEX IF NOT EXISTS idx_cached_queries_expires_at ON cached_queries(expires_at);
CREATE INDEX IF NOT EXISTS idx_cached_queries_query_type ON cached_queries(query_type);

-- RLS — completely locked down for public access; only service_role can operate
ALTER TABLE cached_queries ENABLE ROW LEVEL SECURITY;

-- No public policies — the table is invisible to anon and authenticated users.
-- All cache operations must go through the Supabase service_role key.
--
-- If you need admin access from the dashboard you can add:
--   CREATE POLICY "Service role full access" ON cached_queries
--     FOR ALL USING (auth.role() = 'service_role')
--     WITH CHECK (auth.role() = 'service_role');
--
-- But by default, service_role bypasses RLS entirely, so no explicit
-- policy is required.


-- ═══════════════════════════════════════════════════════════════════════════════
-- 7. SCIENTIFIC_SYNONYMS
-- ═══════════════════════════════════════════════════════════════════════════════
-- Maps alternate names, brand names, and common misspellings to their
-- canonical scientific term. Used for query normalization so that
-- "warfarin" and "Coumadin" resolve to the same entity.

CREATE TABLE IF NOT EXISTS scientific_synonyms (
  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  term        TEXT NOT NULL,
  canonical   TEXT NOT NULL,
  term_type   TEXT CHECK (term_type IN ('drug', 'herb', 'compound', 'phytochemical')),
  created_at  TIMESTAMPTZ DEFAULT now(),

  -- Prevent duplicate term+type entries (e.g. two "aspirin" drug rows)
  UNIQUE (term, term_type)
);

COMMENT ON TABLE scientific_synonyms IS 'Synonym lookup table for normalizing drug/herb/compound names to their canonical scientific terms.';

-- Indexes
CREATE INDEX IF NOT EXISTS idx_scientific_synonyms_term       ON scientific_synonyms(term);
CREATE INDEX IF NOT EXISTS idx_scientific_synonyms_canonical  ON scientific_synonyms(canonical);
CREATE INDEX IF NOT EXISTS idx_scientific_synonyms_term_type  ON scientific_synonyms(term_type);
CREATE INDEX IF NOT EXISTS idx_scientific_synonyms_term_type_combo ON scientific_synonyms(term, term_type);

-- RLS — public read-only; only service_role can insert/update/delete
ALTER TABLE scientific_synonyms ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public read scientific synonyms"
  ON scientific_synonyms FOR SELECT
  USING (true);


