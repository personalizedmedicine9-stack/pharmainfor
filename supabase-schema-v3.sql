-- TRIGGER: Auto-create user_profile on signup
-- ═══════════════════════════════════════════════════════════════════════════════
-- When a new row is inserted into auth.users (i.e. a user signs up),
-- this trigger automatically creates a corresponding user_profiles row
-- so the application always has profile data to work with.

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  INSERT INTO public.user_profiles (id, email, display_name)
  VALUES (
    NEW.id,
    NEW.email,
    COALESCE(NEW.raw_user_meta_data->>'display_name', NEW.raw_user_meta_data->>'full_name', split_part(NEW.email, '@', 1))
  );
  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION public.handle_new_user() IS
  'Trigger function: automatically creates a user_profiles row when a new auth.users row is inserted.';

-- Drop the trigger first if it already exists (idempotent)
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_new_user();


-- ═══════════════════════════════════════════════════════════════════════════════
-- UTILITY: Clean up expired cache entries
-- ═══════════════════════════════════════════════════════════════════════════════
-- Convenience function to purge stale cached_queries rows.
-- Call periodically (e.g. via pg_cron or a serverless edge function).

CREATE OR REPLACE FUNCTION public.clean_expired_cache()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  DELETE FROM public.cached_queries WHERE expires_at < now();
END;
$$;

COMMENT ON FUNCTION public.clean_expired_cache() IS
  'Utility: deletes all expired rows from cached_queries. Intended to be called periodically.';


