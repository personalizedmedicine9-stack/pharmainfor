-- ═══════════════════════════════════════════════════════════════════════════════
-- 4. SAVED_REPORTS
-- ═══════════════════════════════════════════════════════════════════════════════
-- User bookmarks / saved reports. Each row links a user to a specific
-- interaction or pharmacology report they want to revisit.

CREATE TABLE IF NOT EXISTS saved_reports (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id         UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  report_id       TEXT NOT NULL,
  report_type     TEXT NOT NULL CHECK (report_type IN ('interaction', 'pharmacology')),
  report_data     JSONB DEFAULT '{}'::jsonb,
  drug_name       TEXT,
  herb_name       TEXT,
  evidence_score  INTEGER,
  evidence_level  TEXT,
  confidence      TEXT,
  created_at      TIMESTAMPTZ DEFAULT now(),

  -- Prevent duplicate saves: one user cannot save the same report twice
  UNIQUE (user_id, report_id)
);

COMMENT ON TABLE saved_reports IS 'User bookmarked/saved reports. Each user can save a given report only once (UNIQUE on user_id + report_id).';

-- Indexes
CREATE INDEX IF NOT EXISTS idx_saved_reports_user_id    ON saved_reports(user_id);
CREATE INDEX IF NOT EXISTS idx_saved_reports_report_id  ON saved_reports(report_id);
CREATE INDEX IF NOT EXISTS idx_saved_reports_type       ON saved_reports(report_type);
CREATE INDEX IF NOT EXISTS idx_saved_reports_created    ON saved_reports(created_at DESC);

-- RLS
ALTER TABLE saved_reports ENABLE ROW LEVEL SECURITY;

-- Users can only read their own saved reports
CREATE POLICY "Users read own saved reports"
  ON saved_reports FOR SELECT
  USING (user_id = auth.uid());

