-- ═══════════════════════════════════════════════════════════════════════════════
-- 1. USER_PROFILES
-- ═══════════════════════════════════════════════════════════════════════════════
-- Extends Supabase Auth users with application-level profile data.
-- One row per auth.users entry; auto-created on signup via trigger below.

CREATE TABLE IF NOT EXISTS user_profiles (
  id          UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  email       TEXT UNIQUE NOT NULL,
  display_name TEXT,
  created_at  TIMESTAMPTZ DEFAULT now()
);

COMMENT ON TABLE user_profiles IS 'Application-level user profiles linked 1:1 to Supabase Auth users.';

-- Indexes
CREATE INDEX IF NOT EXISTS idx_user_profiles_email ON user_profiles(email);

-- RLS
ALTER TABLE user_profiles ENABLE ROW LEVEL SECURITY;

-- Users can only read their own profile
CREATE POLICY "Users can read own profile"
  ON user_profiles FOR SELECT
  USING (id = auth.uid());

-- Users can only update their own profile
CREATE POLICY "Users can update own profile"
  ON user_profiles FOR UPDATE
  USING (id = auth.uid())
  WITH CHECK (id = auth.uid());


-- ═══════════════════════════════════════════════════════════════════════════════
-- 2. INTERACTION_REPORTS
-- ═══════════════════════════════════════════════════════════════════════════════
-- Stores drug-herb interaction reports generated by the search engine.
-- user_id is nullable to support anonymous (unauthenticated) reports.

CREATE TABLE IF NOT EXISTS interaction_reports (
  id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id           UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  drug_name         TEXT NOT NULL,
  herb_name         TEXT NOT NULL,
  interaction_type  TEXT,
  severity          TEXT,
  mechanism         TEXT,
  confidence_score  INTEGER DEFAULT 0,
  evidence_level    TEXT,
  ref_list          JSONB DEFAULT '[]'::jsonb,
  study_results     JSONB DEFAULT '[]'::jsonb,
  fda_data          JSONB,
  sources_used      JSONB DEFAULT '[]'::jsonb,
  evidence_breakdown JSONB DEFAULT '{}'::jsonb,
  top_citation_count INTEGER DEFAULT 0,
  total_studies     INTEGER DEFAULT 0,
  created_at        TIMESTAMPTZ DEFAULT now()
);

COMMENT ON TABLE interaction_reports IS 'Drug-herb interaction reports. user_id is NULL for anonymous submissions.';

-- Indexes — cover common query patterns (by user, by drug+herb, recent first)
CREATE INDEX IF NOT EXISTS idx_interaction_reports_user_id     ON interaction_reports(user_id);
CREATE INDEX IF NOT EXISTS idx_interaction_reports_drug_herb   ON interaction_reports(drug_name, herb_name);
CREATE INDEX IF NOT EXISTS idx_interaction_reports_created     ON interaction_reports(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_interaction_reports_severity    ON interaction_reports(severity);
CREATE INDEX IF NOT EXISTS idx_interaction_reports_evidence    ON interaction_reports(evidence_level);

-- RLS
ALTER TABLE interaction_reports ENABLE ROW LEVEL SECURITY;

-- Anyone can read interaction reports (public knowledge base)
CREATE POLICY "Public read interaction reports"
  ON interaction_reports FOR SELECT
  USING (true);

-- Authenticated users can insert their own reports
CREATE POLICY "Users insert own interaction reports"
  ON interaction_reports FOR INSERT
  WITH CHECK (user_id = auth.uid() OR user_id IS NULL);

-- Users can update only their own reports
CREATE POLICY "Users update own interaction reports"
  ON interaction_reports FOR UPDATE
  USING (user_id = auth.uid())
  WITH CHECK (user_id = auth.uid());

-- Users can delete only their own reports
CREATE POLICY "Users delete own interaction reports"
  ON interaction_reports FOR DELETE
  USING (user_id = auth.uid());


